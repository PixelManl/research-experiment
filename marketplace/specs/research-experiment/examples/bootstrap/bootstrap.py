import argparse
import os
from pathlib import Path

def create_scaffold(package_name: str, task_slot: str):
    root = Path.cwd()
    
    print(f"🚀 初始化科研项目脚手架...")
    print(f"📦 包名 (Package): {package_name}")
    print(f"🎯 初始任务槽 (Task-slot): {task_slot}")
    
    # 1. src-layout
    src_dir = root / "src" / package_name
    src_dir.mkdir(parents=True, exist_ok=True)
    (src_dir / "__init__.py").touch()
    
    for d in ["policy", "runner"]:
        (src_dir / d).mkdir(parents=True, exist_ok=True)
        (src_dir / d / "__init__.py").touch()
    
    for mod in ["process.py", "metrics.py", "diagnostics.py", "numerics.py", "schema.py", "validate.py", "plotting.py", "provenance.py"]:
        (src_dir / mod).touch()
        
    # 2. Configs
    configs_dir = root / "configs"
    for sub in ["task", "experiment", "debug", "hydra", "truth"]:
        (configs_dir / sub).mkdir(parents=True, exist_ok=True)
    
    # 生成基础 hydra 配置占位
    config_yaml = f"""# MAIN CONFIG
# Truth baseline: configs/truth/config.yaml
# Validate this file against the truth baseline before formal runs.
defaults:
  - task: {task_slot}
  - experiment: default
  - debug: off
  - _self_

run:
  name: default
  seed: 0

validation:
  modified_original: false
  modification_reason: ""
  modification_date: ""
"""
    truth_yaml = config_yaml.replace(
        "# MAIN CONFIG\n# Truth baseline: configs/truth/config.yaml\n# Validate this file against the truth baseline before formal runs.",
        "# CONFIG TRUTH\n# Baseline default config. Edit only with explicit human approval.",
    )
    (configs_dir / "config.yaml").write_text(config_yaml)
    (configs_dir / "truth" / "config.yaml").write_text(truth_yaml)
    (configs_dir / "truth" / "config_truth.md").write_text(
        f"# Config Truth\n\nTask slot: `{task_slot}`\n\nFill source links and parameter checks before formal experiments.\n"
    )
    (configs_dir / "task" / f"{task_slot}.yaml").write_text(f"task:\n  slot: {task_slot}\n")
    (configs_dir / "experiment" / "default.yaml").write_text("experiment:\n  name: default\n")
    (configs_dir / "debug" / "off.yaml").write_text("debug:\n  enabled: false\n  mode: off\n")
    (configs_dir / "debug" / "smoke.yaml").write_text("debug:\n  enabled: true\n  mode: smoke\n  max_steps: 2\nrun:\n  name: smoke\n")
    (configs_dir / "debug" / "dry_run.yaml").write_text("debug:\n  enabled: true\n  mode: dry_run\nrun:\n  name: dry-run\n")
    (configs_dir / "hydra" / "default.yaml").write_text("hydra:\n  run:\n    dir: outputs/${task.slot}/${now:%Y-%m-%d}/${now:%H%M%S}-${run.name}\n  job:\n    chdir: false\n")
    (configs_dir / "schema.py").write_text("# dataclass / structured config roots\n")
    
    # 3. Task slots directories
    for p in ["scripts", "tests"]:
        task_dir = root / p / task_slot
        task_dir.mkdir(parents=True, exist_ok=True)
        (task_dir / "index.md").write_text(f"# {p.capitalize()} Index for {task_slot}\n")

    outputs_dir = root / "outputs"
    outputs_dir.mkdir(parents=True, exist_ok=True)
    (outputs_dir / "index.md").write_text(
        "# Outputs\n\n"
        "<!-- GENERATED by scripts/common/runs.py render - DO NOT EDIT BY HAND -->\n\n"
        "| Task slot | Canonical | Validity | Runs | Last activity |\n"
        "|---|---|---|---|---|\n"
    )
        
    # 4. Data
    (root / "data" / "processed").mkdir(parents=True, exist_ok=True)
    
    # 5. Docs & Research Log
    main_dir = root / "docs" / "main"
    main_dir.mkdir(parents=True, exist_ok=True)
    (main_dir / "main.md").write_text("# 实验主日志 (Main Journal)\n\n## YYYY-MM-DD — 项目初始化\n\n### 实验叙事\n项目骨架建立，准备进行基线测试。\n")
    
    log_dir = root / "docs" / "research-log"
    log_dir.mkdir(parents=True, exist_ok=True)
    
    for md in ["index.md", "baselines.md", "source-of-truth.md", "invalidated-results.md", "reviewer-objections.md"]:
        (log_dir / md).touch()
        
    for sub in ["decisions", "tasks", "reports", "equation-maps"]:
        (log_dir / sub).mkdir(parents=True, exist_ok=True)
        
    (log_dir / "tasks" / f"{task_slot}.md").write_text(f"# Task Ledger: {task_slot}\n")

    print(f"✅ 脚手架生成完毕！请运行 `pip install -e .` (如果有 pyproject.toml) 来安装包。")

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Bootstrap Research-Experiment Scaffold")
    parser.add_argument("--package", required=True, help="真实的 Python 包名 (如 zero_ucar)")
    parser.add_argument("--task-slot", required=True, help="初始的实验任务槽名 (如 default_env_test)")
    args = parser.parse_args()
    
    create_scaffold(args.package, args.task_slot)
